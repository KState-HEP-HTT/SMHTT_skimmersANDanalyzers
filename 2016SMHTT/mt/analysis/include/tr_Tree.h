void fillNNTree(TTree* namu, TLorentzVector tau1, float charge1, float decaymode, TLorentzVector tau2, float charge2, TLorentzVector jet1, TLorentzVector jet2, TLorentzVector mymet, float mymjj, float mypt_sv, float mym_sv, float mymt, float mynjets, float bpt_1, float beta_1, float bphi_1, float bpt_2, float beta_2, float bphi_2,TLorentzVector Higgs, bool is_0jet, bool is_boosted, bool is_VBF, bool signalRegion, bool qcdRegion, bool wRegion, bool wsfRegion, bool qcdCR, float myevtwt, float myME_sm_VBF, float myME_sm_ggH, float myME_bkg){
  evtwt = myevtwt;
  t1_pt = tau1.Pt();
  t1_eta = tau1.Eta();
  t1_phi = tau1.Phi();
  t1_mass = tau1.M();
  t1_charge = charge1;
  t1_decaymode = decaymode;
  mu_pt = tau2.Pt();
  mu_eta = tau2.Eta();
  mu_phi = tau2.Phi();
  mu_mass = tau2.M();
  mu_charge = charge2;

  j1_pt = jet1.Pt();
  j1_eta = jet1.Eta();
  j1_phi = jet1.Phi();
  j2_pt = jet2.Pt();
  j2_eta = jet2.Eta();
  j2_phi = jet2.Phi();

  met = mymet.Pt();
  metphi = mymet.Phi();
  mjj = mymjj;
  njets = mynjets;
  pt_sv = mypt_sv;
  m_sv = mym_sv;
  mt = mymt;
  ME_sm_VBF = myME_sm_VBF;
  ME_sm_ggH = myME_sm_ggH;
  ME_bkg = myME_bkg;

  b1_pt = bpt_1;
  b1_eta = beta_1;
  b1_phi = bphi_1;
  b2_pt = bpt_2;
  b2_eta = beta_2;
  b2_phi = bphi_2;

  higgs_pT = Higgs.Pt();
  higgs_m = Higgs.M();
  hjj_pT = (Higgs + jet1 + jet2).Pt();
  hjj_m = (Higgs + jet1 + jet2).M();
  dEtajj = fabs(jet1.Eta()-jet2.Eta());
  dPhijj = fabs(jet1.Phi()-jet2.Phi());//jet1.DeltaPhi(jet2);
  cat_0jet = is_0jet;
  cat_boosted = is_boosted;
  cat_vbf = is_VBF;
  is_signal = signalRegion;
  is_qcd = qcdRegion;
  is_w = wRegion;
  is_wsf = wsfRegion;
  is_qcdCR = qcdCR;
  cat_inclusive = false;
  if (cat_0jet || cat_boosted || cat_vbf) cat_inclusive = true;

  namu->Fill();
}

